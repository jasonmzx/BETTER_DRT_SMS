let transfer_check = (trip_data, daw_list) => {
  let same_trip = {
    today: [],
    tmrw: []
  }  

  trip_data.forEach( (trip,index) => {
    if(trip.serviceId === daw_list[0] ){
      same_trip.today.push([]);
    } else { same_trip.tmrw.push([])}
    console.log('--------------------');
    //Basically filter function..
    trip_data.forEach( (filt_trip,filt_index) => {
      if(trip.serviceId === filt_trip.serviceId 
        && trip.arrivalTime === filt_trip.arrivalTime 
        && trip.tripId.slice(5,10) === filt_trip.tripId.slice(5,10))
      {
 
        if(filt_trip.serviceId === daw_list[0] ){
          if( !((same_trip.today.filter(tuple => tuple[0] === filt_index || tuple[1] === filt_index)).length)  ){
            same_trip.today[index].push(filt_index); 
          }
        } //else { same_trip.tmrw[index].push(filt_index);}
      }
      
    });


  });

  return same_trip;
}

